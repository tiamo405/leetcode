class Solution(object):
    def check(self, s, i, words):
        tmp = s[i:i+len(words)*len(words[0])]
        arr = []
        for i in range(0, len(tmp)-len(words[0])+1, len(words[0])):
            arr.append(tmp[i:i+len(words[0])])
        arr = sorted(arr)
        words = sorted(words)

        return arr == words
    def findSubstring(self, s, words):
        """
        :type s: str
        :type words: List[str]
        :rtype: List[int]
        """
        ans = []
        len_text= len(words[0])
        n = len(words)
        for i in range(0, len(s)-n*len_text+1):
            if self.check(s, i, words) :
                ans.append(i)
        return ans
